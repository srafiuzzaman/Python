# Learn to use string concatenation and the new line escape sequence to format strings in Python.
# Use \n to add another line of "Hello world".
# So the resulting output looks like this:
"""
Hello world!
Hello world!
Hello world!
"""

print("Hello world!\nHello world!\nHello world!")

# Add a space between the strings
# So there is a space between the string Hello and Sahad when the print statement runs.
# The output should look like this:
"""
Hello Sahad
"""

print("Hello" + " " + "Sahad")