# Basic Operations
# Learn to use the basic mathematical operators, + , - ,*, /, //, and **

print("My age: " + str(12))     # 12
print(123 + 456)                # 579
print(7 - 3)                    # 4
print(3 * 2)                    # 6
print(6 / 3)                    # 2.0
print(6 // 3)                   # 2
print(5 / 3)                    # 1.6666666666666667
print(5 // 3)                   # 1
print (2**3)                    # 8

