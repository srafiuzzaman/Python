# For Loops
# Loops allow us to tell the computer to repeated actions without having to write repeated code. If we wanted the
# computer to print out 1 through 100, it would be painful to type a print statement for every number, or even just
# typing out all the numbers 1 through to 100. Loops allow us to create a rule and the computer
# can follow it to do a repeated action.

fruits = ["Apple", "Peach", "Pear"]

for fruit in fruits:
    print(fruit)
    print(fruit + " Pie")

print(fruits)
