# For Loop with Range
for number in range(1, 10):
    print(number)
print("\n")

for number in range(1, 11):
    print(number)
print("\n")

for number in range(1, 11, 3):
    print(number)
print("\n")

total = 0
for number in range(1, 101):
    total += number
print(f"{total}\n")